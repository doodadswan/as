<p>Amaterasu was the sun goddess of the Shinto religion in Japan. She was an owner of a heavenly garden, and her presence brought happiness to everyone.  </p>
<p>One day, Amaterasu's brother, the storm deity Susanoo, blew unusually strong winds which ruined Amaterasu's garden. This made Amaterasu so sad that she hid in a cave, causing Japan to be dark and sunless for weeks.</p>
<p>One day, the other gods and goddesses of Japan decided to trick Amaterasu to try to get her to come out of the cave where she was hiding. They began to dance, sing, and play music. They also hung a mirror on a limb of a tree outside of the cave. 
Amaterasu, hearing all of the celebrating, decided to come out to see why everyone was so happy without her. The gods told her that they had found a new sun goddess.</p>
