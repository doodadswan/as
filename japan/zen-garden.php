<p>Zen gardens are Japanese rock gardens of a peaceful nature. Zen gardens are made of two main elements: sand and rocks. Sand is artfully raked daily in patterns that evoke the ripples of the sea. Gravel may be used in place of sand. Zen gardens are pits of sand or gravel with carefully placed islands of rock. Zen gardens have a peaceful, or Zen-like, atmosphere.</p>
<p>Many small versions of Zen gardens are made for personal use, such as for at home or for your desk at work. Other elements of Zen gardens include bridges, islands, flowers or plants, trees, water, and waterfalls. Iris, lotus, or lily flowers may also be used.</p>
