<p><img src="images/shrine.jpg" alt="" width="392" height="277" class="floatleft" />The Ise Grand Shrine is a Shinto shrine in Japan that is dedicated to the sun goddess Amaterasu. It is one of Shintoism's most sacred shrines. The sacred mirror of Amaterasu is housed in the Ise Shrine. </p>
<p>Shikinen Sengu is the name for the ceremony in Japan in which the Ise Grand Shrine is rebuilt every twenty years. This year (2013) happens to be the 20th year! The first Shikinen Sengu took places 1300 years ago. The ceremony includes renewal of buildings (shelter) as well as the renewal of the treasures (clothing) and the offering of the first fruits (food). The renewal of the buildings and treasures involves the wish that Japanese traditional culture should be transmitted to the next generation.</p>
