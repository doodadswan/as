   	<h1>STEM Vocabulary</h1>
	  <p><span class="vocab">2D Sketch</span> – A sketch that is made on a flat piece of paper.</p>
	  <p><span class="vocab">3D Object</span> – A three dimensional object is created by specific software, where the object resides on a three dimensional surface that incorporates space.</p>
	  <p><span class="vocab">Automation</span> – The use of computers or machines to control machine operations and make a product. </p>
	  <p><span class="vocab">Brainstorming</span> – A group or individual activity where you try to find the answer for a specific problem by gathering a list of ideas spontaneously. </p>
	  <p><span class="vocab">Computer Aided Design</span> – The use of computer systems to assist in the creation, modification, analysis, or optimization of a design. </p>
	  <p><span class="vocab">Constraints</span> – Limits on a product’s design.</p>
	  <p><span class="vocab">Dimensions</span> – A measurement of the size of something in a particular direction, such as the length, width, height, or diameter.</p>
	  <p><span class="vocab">Engineer</span> – Someone who applies scientific and design principles to create practical solutions for problems. </p>
	  <p><span class="vocab">Engineering Design Process</span> – The engineering design process is a series of steps that engineers and designers use to guide them as they solve problems. This process is often repeated as many times as needed, making improvements along the way. </p>
	  <p><span class="vocab">Innovation</span> – The process of improving a design. </p>
    <p><span><span class="vocab">Interface</span></span> – A point where two systems meet and interact. </p>
	  <p><span class="vocab">Isometric Drawing</span> – A drawing where three sides of the object are shown in a single view. </p>
	  <p><span class="vocab">Orthographic Projection</span> – A kind of drawing that shows each surface of the object separately, as if you were looking straight at it. </p>
	  <p><span class="vocab">Metric System</span> –  The international system of unit measurement. </p>
	  <p><span class="vocab">Programming</span> – The act or process of sending an organized list of instructions that when executed causes the computer to behave in a predetermined manner. </p>
	  <p><span class="vocab">Prototype</span> – Is the first working version of the designer’s solution. </p>
	  <p><span class="vocab">Specifications</span> – Criteria for a product to be successful (useful, practical, cost effective, performance, details, etc.). </p>
	  <p><span class="vocab">Technology</span> – The knowledge and process of using the laws of nature to solve problems by designing and making products or structures to satisfy human needs and wants. </p>
	  <p><span class="vocab">Trade-Off</span> – The giving up one thing for another. </p>
