		<h1>Aerospace Engineer</h1>
		<ul>
		<li>
		Designs aircraft
		</li>
		<li>Divided into two major and overlapping branches: aeronautical engineering and astronautical engineering. Aeronautics 
deals with aircraft that operate in Earth's 
atmosphere, and astronautics 
deals with spacecraft that operate outside the Earth's atmosphere. </li>
		</ul>
	</section>
	<section>
		<h1>Mining Engineer</h1>
		<ul>
		<li>
		Develops techniques for mining
		</li>
		<li>More specifically, mining engineering is an engineering discipline that involves the practice, the theory, the science, the technology, and application of extracting and processing minerals from a naturally occurring environment. </li>
		</ul>
	</section>
	<section>
		<h1>Nuclear Engineer</h1>
		<ul>
		<li>
		Designs power plants and nuclear systems</li>
		<li>Includes the interaction and maintenance of systems and components like nuclear reactors, nuclear power plants, and/or nuclear weapons       	    </li>
		<li>Also incorporates the study of medical applications </li>
		</ul>
	</section>
	<section>
		<h1>Chemical Engineer</h1>
		<ul>
		<li>
		Designs types of chemical processes to work in a wide variety of industries
		</li>
		<li>Combines natural sciences and life sciences together with mathematics and economics to produce, transform, transport, and properly use chemicals, materials and energy </li>
		</ul>
	</section>
	<section>
		<h1>Marine Engineer</h1>
		<ul>
		<li>
		Engineering that deals with drilling, transportation across the ocean</li>
		<li>Specifically, marine engineering is the discipline of applying engineering sciences, mostly mechanical and electrical engineering, to the development, design, operation and maintenance of watercraft propulsion and on-board systems </li>
		</ul>
	</section>
	<section>
		<h1>Electrical Engineer</h1>
		<ul>
		<li>
		Engineering dealing with designs of robots, MP3 players, electronics, and
communication systems
		</li>
		<li>Field of engineering that generally deals with the study and application of electricity, electronics, and electromagnetism       	    </li>
		<li>The Woz – Inventor of Apple Computers was an electrical engineer</li>
		</ul>
    </section>
	<section>
		<h1>Mechanical Engineer</h1>
		<ul>
		<li>
		Engineering that deals with inventors of cutting edge innovations or devices</li>
		<li>Mechanical engineering is a discipline of engineering that applies the principles of engineering, physics and materials science for analysis, design, manufacturing, and maintenance of mechanical systems</li>
		<li>SolidWorks is designed specifically for the mechanical engineering field</li>
		</ul>
	</section>
	<section>
		<h1>Computer Engineer</h1>
		<ul>
		<li>
		Engineering dealing with developing computer science and computer systems</li>
		<li>Discipline that integrates several fields of electrical engineering and computer science required to develop computer hardware and software </li>
		</ul>
	</section>
