<h1>Artist Biography</h1>
<p class="artist">Alex Swan, a native of Georgia, has been intrigued in art since he was in high school.  Until then, he never really connected with any other subject and once he discovered the world of art he did not want to stop making art.  He went on to receive a BFA in Painting from Georgia Southern University in 1997 and received the Robert J. Focht memorial scholarship there in his senior year.  After graduation, Alex was lured into the world of graphics and information technology where he worked in various capacities for fifteen years.  After fifteen years of working in information technology, Alex began to grow disillusioned with his current line of work and decided to seek a degree in art education.  Currently a pre-service teacher with a focus on culturally responsive pedagogy, Alex shares his passion for art and to students, art lovers, and generally anyone who will listen.  He currently resides in Atlanta, Georgia with his wife and three children and focuses on drawing, painting, and creating digital artwork.  He exhibits his work regionally.</p>
