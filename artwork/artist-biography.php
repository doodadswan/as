<h1>Artist Biography</h1>
<p class="artist">Alex Swan, a native of Georgia, has been intrigued in art since he was in high school.  Until then, he never really connected with any other subject.  He went on to receive a BFA in Painting from Georgia Southern University in 1997 and received the Robert J. Focht memorial scholarship in his senior year.  After graduation, Alex was entered the world of graphics and information technology where he worked in various capacities for fifteen years.  After fifteen years of working in information technology, Alex went back to school to become an educator.  Currently he serves as the Engineering and Technology teacher at McClure Middle School in Cobb County.  He currently resides in Atlanta, Georgia with his wife and three children and focuses on art, design, and technology.</p>
