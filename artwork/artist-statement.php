<h1>Artist Statement</h1>
<p class="artist">I often use my art as an outlet to express my feelings and what I am experiencing personally.  This is something that has helped me grow and develop as an artist over the years.  My art is very expressionistic, yet it varies stylistically ranging from abstraction to figural.  I love working with oil paint, collage, and digital art, as well as utilizing various technologies, ranging from Adobe Creative Suite, to more traditional technologies, such as printmaking, and imploring mixed media approaches.  I am strongly influenced by Surrealism and Abstract Expressionism, as well as the work of artists ranging from Salvador Dali to Willem DeKooning to Andy Warhol.  I admire how the surrealists and expressionists embraced the subconscious and allowed this to enter into their work.  I am extremely fond of the subtleties of the human form and consider myself a lifelong student of color.  My background includes working in the field of technology, reading books on art history, and studying art education; with that being said, these subjects and this study often find their way into my art.  I consider art a lifelong exploration and enjoy the process as much or more than any of the pieces that come to be along the way. </p>
